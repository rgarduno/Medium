//This file contains examples of the first way to initialize the date constructor
//Given the year, month, day, hour and minutes

new Date(2002,1,30,14,55);
// => Sat Mar 02 2002 14:55:00 GMT-0600 (CST)

new Date(2004,5,17,22,55);
// => Thu Jun 17 2004 22:55:00 GMT-0500 (CDT)

new Date(2017,10,22);
// => Wed Nov 22 2017 00:00:00 GMT-0600 (CST)
